#include "./libfdf.h"

void	fdf_open_window(void)
{
	void	*mlx;
	void	*win;

	mlx = mlx_init();
	win = mlx_new_window(mlx, 800, 600, "Fil de fer du cerveau");
	mlx_hook(win, 17, 0L, &fdf_close_window, NULL);
	mlx_hook(win, 2, 0L, &fdf_close_window, NULL);
	mlx_loop(mlx);
}
